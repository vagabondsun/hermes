    <div class="field field-name-body field-type-text-with-summary field-label-hidden"><div class="field-items"><div class="field-item even" property="content:encoded"><h1>Alterhumanity: Glossary</h1>
<p>The term 'alterhuman' covers a lot of ground. Just because you've hung around one sub-community doesn't mean you've heard of them all, to say nothing of someone who's new to alterhumanity all together. This is a glossary of frequently seen terms for confused and curious parties. Terms highlighted in <b>bold</b> in a definition are defined elsewhere in the document.</p>
<div class="alterhuman">
<h3>Alterhuman</h3>
<p class="definition">Having an identity that is alternative to the common societal idea of 'human'; a person who identifies as such.</p>
<ul><li><a href="http://phasmovore.tumblr.com/post/98482696958/this-will-probably-be-my-last-post-on-semantics">Alter-human/Alternative Humanity Personal Identity "AHPI"</a></li>
<li><a href="http://www.alt-h.net/2017/01/ontological-ambiguity.html">How I Learned To Stop Worrying And Love Ontological Ambiguity</a></li>
</ul></div>
<div class="awakening">
<h3>Awakening</h3>
<p class="definition">The period during which an alterhuman person realizes their identity. One can have several Awakenings, though a few folks prefer to use the term only to refer to their first time realizing their alterhumanity overall.</p>
</div>
<div class="daemian">
<h3>Daemian</h3>
<p class="definition">A person in possession of a <b>daemon</b>. Daemians are not inherently alterhuman but there is an overlap.</p>
</div>
<div class="daemon">
<h3>Daemon</h3>
<p class="definition">A mental construct which serves as the other half of one's mental dialogue. It takes the form of an animal based on the <b>daemian</b>'s personality in relation to real life animal traits and behaviours.</p>
<ul><li><a href="http://daemonpage.com/introduction.php">A basic introduction to the concept of daemons</a></li>
<li><a href="http://daemonsandanalyses.tumblr.com/daemons">All About Daemons</a></li>
</ul></div>
<div class="dysphoria speciesdysphoria">
<h3>Dysphoria</h3>
<p class="definition">A state of great unease or dissatisfaction. Most relevant to alterhumans in the form of <i>species dysphoria</i>, that is, being distressed or uncomfortable about one's percieved or bodily species.</p>
</div>
<div class="extranth">
<h3>Extranth</h3>
<p><i class="synonyms">aka: extranthrope</i></p>
<p class="definition">A nonhuman member of a <b>plural system</b>; a person who experiences their body in <b>headspace</b> being nonhuman.</p>
</div>
<div class="fableing">
<h3>Fableing</h3>
<p class="definition">A fictional identity facet in a <b>median system</b>, who has experiences and feelings in common with both <b>fictives</b> and <b>fictionkin</b> in a way that makes them feel somewhere between or adjacent to the two.</p>
</div>
<div class="fictionkin fictionkind fickin otakukin otakin">
<h3>Fictionkin</h3>
<p><i class="synonyms">aka: fictionkind, (older) otakukin</i></p>
<p class="definition">A person who identifies as an entity from fiction. One could consider themselves a specific character, a member of a fictional race, or simply a being from a fictional world.</p>
<ul><li><a href="http://fictionkin.com/about-fictionkin/">About Fictionkin</a></li>
<li><a href="http://fromfiction.tumblr.com/basics">Fromfiction's Fictionkin Basics</a></li>
</ul></div>
<div class="fictive fictionalintroject">
<h3>Fictive</h3>
<p><i>aka: fictional introject</i></p>
<p class="definition">A <b>headmate</b> from a fictional source. Can be a specific character, of a fictional species, or simply from a fictional world.</p>
</div>
<div class="fictosexual fictoromantic ficto">
<h3>Fictosexual</h3>
<p><i>aka: fictoromantic, ficto</i></p>
<p class="definition">Experiencing attraction to fictional characters.</p>
</div>
<div class="flicker fictionflicker">
<h3>Flicker</h3>
<p><i class="synonyms">aka: fictionflicker</i></p>
<p class="definition">As it was originally coined, this referred to a fictional identity which is temporarily taken on, usually after consuming the media from which the identity stems. In more recent days it has come to mean any fictional identity phenomena which can't otherwise easily be categorized.</p>
</div>
<div class="front fronting frontroom infront">
<h3>Front</h3>
<p class="definition">The mental space from where a member of a <b>plural system</b> controls the system's body. When in control, the entity can be said to be "fronting" or "in front".</p>
</div>
<div class="furry">
<h3>Furry</h3>
<p class="definition">A subculture based around the appreciation of anthropomorphic animals. Furries are not inherently alterhuman, though some may consider themselves alterhuman if they identify as their fursona more than their earthly body. This is much more prolific in some parts of the community than others (see <b>postfurry</b>).</p>
<ul><li><a href="http://whatarefurries.com/">whatarefurries.com</a></li>
<li><a href="http://en.wikifur.com/wiki/Furry">Furry on WikiFur</a></li>
</ul></div>
<div class="fursona">
<h3>Fursona</h3>
<p class="definition">An anthropomorphic animal character which is usually somewhat representative of its creator. Some furries identify more with their fursona than their earthly body, and consider themselves alterhuman because of this.</p>
</div>
<div class="godshard">
<h3>Godshard</h3>
<p class="definition">A person who considers themselves to be a smaller, independent part of a still-extant god.</p>
</div>
<div class="headmate alter">
<h3>Headmate</h3>
<p><i class="synonyms">aka: alter</i></p>
<p class="definition">Someone who shares a brain/body with someone else; a member of a <b>plural system</b>.</p>
</div>
<div class="headspace">
<h3>Headspace</h3>
<p><i class="synonyms">aka: inworld, wonderland</i></p>
<p class="definition">A shared mental space where members of a plural system reside when not <b>in front</b>. The term 'wonderland' is typically used by <b>tulpamancers</b>.</p>
</div>
<div class="hearthome">
<h3>Hearthome</h3>
<p class="definition">A location, or type of location, real or otherwise, that a person has a strong emotional connection to, considering it 'home' despite not having been raised or spending much time there.</p>
</div>
<div class="median mediansystem">
<h3>Median</h3>
<p class="definition">Describes any experience that is in-between being <b>singlet</b> and <b>multiple</b>. Median systems exist on a continuum between being one person in a body and being multiple distinct people in a body.</p>
<ul><li><a href="http://astraeasweb.net/plural/mediantalk.html">Median Talk</a></li>
</ul></div>
<div class="medusan">
<h3>Medusan</h3>
<p class="definition">An orientation label for alterhuman/nonhumans who are sexually and/or romantically attracted to other alterhumans and/or nonhumans. this can mean monsters, aliens, or mythical creatures - any intelligent nonhuman creature.</p>
<ul><li><a href="https://queer--buccaneer.tumblr.com/post/168973295337/medusan">Coining post by queer--buccaneer on tumblr</a></li>
</ul></div>
<div class="multiple multiplesystem multiplicity">
<h3>Multiple System</h3>
<p class="definition">A collection of entities sharing a body. Systems are not inherently alterhuman, but many consider themselves such on the basis of their multiplicity alone.</p>
<ul><li><a href="http://www.karitas.net/blackbirds/layman/">The Layman's Guide To Multiplicity</a></li>
<li><a href="http://healthymultiplicity.com/">healthymultiplicity.com</a></li>
</ul></div>
<div class="nonhuman">
<h3>Nonhuman</h3>
<p class="definition">In an alterhuman context, someone who identifies as something other than human. Can be used to refer to the <b>otherkin</b> and <b>therian</b> communities collectively, or can refer to someone who identifies as nonhuman outside of those communities.</p>
</div>
<div class="otherhearted animalhearted fictionhearted hearted">
<h3>Otherhearted</h3>
<p><i class="synonyms">aka: animalhearted, fictionhearted</i></p>
<p class="definition">Having a strong identification with or connection to an entity while maintaining an identity separate from it. Is commonly described as a family-like affinity or being 'almost' the entity but not quite.</p>
<ul><li><a href="http://liongoatsnake.tumblr.com/post/94999217300/the-hisotry-of-the-term-animal-hearted">The History of the Term Animal-Hearted</a></li>
</ul></div>
<div class="otherkin otherkind otherkinity">
<h3>Otherkin</h3>
<p><i class="synonyms">aka: otherkind</i></p>
<p class="definition">A person who identifies on an integral and subjective level as nonhuman, specifically as a mythical or non-earthly creature.</p>
<ul><li><a href="http://frameacloud.com/otherkin/nonfiction-otherkin/introduction-otherkin/">A Simple Introduction to Otherkin and Therianthropes</a></li>
</ul></div>
<div class="copinglink c'link c-link link purposelink otherlink">
<h3>Otherlink</h3>
<p><i class="synonyms">aka: copinglink, c'link, c-link, link, purposelink</i></p>
<p class="definition">A more voluntary identity taken up for a specific purpose, generally animal, mythical or fictional in nature. Though the formation of the identity may be unintentional/accidental, it generally requires a conscious effort to maintain.</p>
<ul><li><a href="https://copinglink-love.tumblr.com/def">What's A Copinglink?</a></li>
</ul></div>
<div class="osor os/or objectum objectumsexual objectumromantic">
<h3>OS/OR</h3>
<p><i class="synonyms">aka: objectum</i></p>
<p class="definition">Objectum-romantic and/or objectum-sexual. Experiencing attraction to inanimate objects.</p>
<ul><li><a href="http://an-actual-objectum-sexual.tumblr.com/OS">About Objectum Sexuality</a></li>
<li><a href="https://os-positive.tumblr.com/">OS Positive</a></li>
</ul></div>
<div class="phantomlimbs astrallimbs otherlimbs metalimbs">
<h3>Phantom Limbs</h3>
<p><i class="synonyms">aka: astral limbs, otherlimbs, metalimbs</i></p>
<p class="definition">Body parts which are perceived by the mind but don't physically exist. When involuntary, they are generally somewhat representative of the identity of the person experiencing them. However, they are easy to purposefully induce for alterhumans and humans alike.</p>
</div>
<div class="plurality plural pluralsystem">
<h3>Plural</h3>
<p class="definition">An umbrella term for <b>median</b> and <b>multiple</b> experiences.</p>
</div>
<div class="phytan phytanthrope plantkin">
<h3>Phytanthrope</h3>
<p><i class="synonyms">aka: (colloquially) plantkin</i></p>
<p class="definition">A person who identifies on an integral and subjective level as nonhuman, specifically as a kind of plant.</p>
</div>
<div class="polymorph">
<h3>Polymorph</h3>
<p class="definition">A specific kind of shapeshifting creature, typically defined by a lack of a 'true' form other than shapeless energy, and a habit of subconciously mirroring the traits of other entities.</p>
</div>
<div class="postfurry">
<h3>Postfurry</h3>
<p class="definition">A subsection of the furry fandom with a specific interest in pushing the boundaries of what it means to be furry. Many postfurries display an above average level of identification as/with their <b>fursona</b>, interrogative attitudes towards 'human' as a category, and an overall similar ethos to the alterhuman community.</p>
<ul><li><a href="http://en.wikifur.com/wiki/Postfurry">Postfurry on WikiFur</a></li>
<li><a href="http://wiki.postfurry.net/wiki/Main_Page">The Postfurry Wiki</a></li>
</ul></div>
<div class="shift mentalshift phantomshift astralshift">
<h3>Shift</h3>
<p class="definition">A phenomenon in which a person's perception 'shifts' to be more like that of their alterhuman identity.</p>
</div>
<div class="singlet">
<h3>Singlet</h3>
<p class="definition">Someone who is not <b>multiple</b> or <b>median</b>.</p>
</div>
<div class="soulbond sb">
<h3>Soulbond</h3>
<p class="definition">
1) A literal bond between souls, akin to the concept of soulmates or twin flames.<br/>
2) An external entity with which someone has a mental connection, usually fictional. Can range from being a 'muse' to an actual <b>headmate</b>. This version is typically distinguished by spelling it SoulBond, or simply calling it 'SB'.</p>
<ul><li><a href="www.soulbonding.org/app.php/page/about">The Living Library</a></li>
<li><a href="http://soulbonding.tripod.com/soulbonding_intro.htm">.soulbonding//an_introduction</a></li>
</ul></div>
<div class="soulbonder">
<h3>SoulBonder</h3>
<p class="definition">A person who is connected to one or more <b>SoulBonds</b>.</p>
</div>
<div class="switch switching">
<h3>Switching</h3>
<p class="definition">The act of one <b>headmate</b> in a <b>plural system</b> taking the place of another in controlling their body; changing who is <b>in front</b>. </p>
</div>
<div class="synpath">
<h3>Synpath</h3>
<p class="definition">An entity that one identifies with or has a strong empathetic connection to. There is some debate as to what the difference between being <b>otherhearted</b> and having a synpath is - plenty of people use both terms and express an intuitive difference between the categories, but can't name <i>what</i> that difference is.</p>
<ul><li><a href="http://stirdrawsandreblaws.tumblr.com/post/114995804896/synpath-faq">Synpath FAQ</a></li>
</ul></div>
<div class="therian therianthrope therianthropy">
<h3>Therian</h3>
<p><i class="synonyms">aka: therianthrope</i></p>
<p class="definition">A person who identifies on an integral and subjective level as nonhuman, specifically a 'real', earthly animal.</p>
<ul><li><a href="http://akhila.feralscribes.org/2004/therianthropy/">Therianthropy on Feralscribes</a></li>
</ul></div>
<div class="theriomythic">
<h3>Theriomythic</h3>
<p class="definition">A person whose identity falls somewhere between otherkinity and therianthropy, in that it is a creature from mythology, but a wild, instinctual or unadvanced one.</p>
<ul><li><a href="http://therian.wikia.com/wiki/Theriomythic">Theriomythic on TherianWiki</a></li>
</ul></div>
<div class="transhumanism transhuman h+">
<h3>Transhumanism</h3>
<p class="definition">An ideology centered around transforming humanity with technology. Transhumanists are not inherently alterhuman, but some people consider themselves alterhuman because of the way this ideology shapes their perception of humanity.</p>
<ul><li><a href="http://whatistranshumanism.org/">whatistranshumanism.org</a></li>
</ul></div>
<div class="transspecies">
<h3>Transspecies</h3>
<p class="definition">Some nonhuman-identifying folk use this label, often to denote that they have, will, or generally desire to modify their bodies to be more nonhuman/less human looking and are often motivated by <b>species dysphoria</b>. Others may use it to describe a feeling of "being [X species] trapped in a human body". Many who use the term do so because they are transgender and their species identity parallels this experience.</p>
<ul><li><a href="http://akhila.feralscribes.org/2013/on-the-appropriation-of-trans-narratives-by-therianthropes/">On the appropriation of trans narratives by therianthropes</a></li>
<li><a href="http://www.adjectivespecies.com/2018/03/12/gender-furry-ii-now-with-more-scales/">Gender: Furry II (Now With More Scales)</a></li>
</ul></div>
<div class="tulpa">
<h3>Tulpa</h3>
<p class="definition">An intentionally created being in a system. Tulpamancers aren't inherently alterhuman, but there is some overlap.</p>
<ul><li><a href="http://tulpa.io/">tulpa.io</a></li>
</ul></div>
<div class="tulpamancer tulpamage tulpamancy tulpamagy">
<h3>Tulpamancer</h3>
<p><i class="synonyms">aka: tulpamage</i></p>
<p class="definition">A person who creates tulpas. Tulpas aren't inherently alterhuman, but there is some overlap.</p>
</div>
<div class="walk-in walkin">
<h3>Walk-In</h3>
<p class="definition">An entity arriving in a body after its birth, typically with memories of being somewhere and someone else. Can join the 'original' as a <b>headmate</b> or replace them entirely, resulting in a <b>singlet</b> walk-in. </p>
<ul><li><a href="http://www.kinhost.org/Main/Walk-Ins">Walk-Ins at Kinhost.org</a></li>
</ul></div>
<div class="xeno xenic xenogender">
<h3>Xenogender</h3>
<p class="definition">A gender that cannot be contained by human understandings of gender; more concerned with crafting other methods of gender categorization and hierarchy such as those relating to animals, plants, or other creatures/things. Often used by alterhumans to describe alter- and non-human experiences of gender.</p>
<ul><li><a href="https://nonbinary.miraheze.org/wiki/Xenogender">Xenongender on NonbinaryWiki</a></li>
</ul></div>
<div class="sandwich sandwichdebate sandwichpuritan sandwichradical" style="display: none;">
<h3>Sandwich</h3>
<p class="definition">Nobody can quite agree what the definition of a sandwich is. The puritan among us will tell you that a sandwich must be a savory filling between two slices of bread. Some radicals are of the opinion that everything counts as a sandwich. Alt+H does not take an official stance one way or the other, and the views of the individual staff members are their own.</p>
<ul><li><a href="https://i.imgur.com/gs6sLDm.jpg">The Sandwich Alignment Chart</a></li>
<li><a href="http://knowyourmeme.com/memes/is-a-hot-dog-a-sandwich">Is A Hotdog A Sandwich? @ KnowYourMeme</a></li>
</ul></div>
